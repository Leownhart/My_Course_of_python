'''
16 - Usando um switch, escreva um programa que leia um inteiro entre 1 e 12 e imprima o mês
correpondente a este numero. Isto é, janeiro se 1, fevereiro se 2, e assim por diante.

Obs: if, elif, else no caso do Python.
'''

opção = int(input('Informe o número do mês número [1 a 12]: '))

if opção == 1:
    print('Janeiro')
elif opção == 2:
    print('Fevereiro')
elif opção == 3:
    print('Março')
elif opção == 4:
    print('Abril')
elif opção == 5:
    print('Maio')
elif opção == 6:
    print('Junho')
elif opção == 7:
    print('Julho')
elif opção == 8:
    print('Agosto')
elif opção == 9:
    print('Novembro')
elif opção == 10:
    print('Setembro')
elif opção == 11:
    print('Outubro')
elif opção == 12:
    print('Dezembro')
