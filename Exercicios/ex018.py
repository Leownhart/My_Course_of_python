from math import sin, cos, tan, radians
ang = float(input('Informe o 창ngulo desejado: '))
sen = sin(radians(ang))
cos = cos(radians(ang))
tan = tan(radians(ang))
print('O 창ngulo de {:.1f} tem o SENO de {:.2f}'.format(ang, sen))
print('O 창ngulo de {:.1f} tem o COSSENO de {:.2f}'.format(ang, cos))
print('O 창ngulo de {:.1f} tem o TANGENTE de {:.2f}'.format(ang, tan))





